#include "libft.h"

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	if (s1 == s2 || !n)
		return (0);
	while (--n && (t_byte)(*s1) == (t_byte)(*s2) && *s1 && *s2)
	{
		s1++;
		s2++;
	}
	return ((t_byte)(*s1) - (t_byte)(*s2));
}
